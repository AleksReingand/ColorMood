package com.alte.colorMood.api.dto;

public class Rgb {
	FractionRgb fraction;
	int r;
	int g;
	int b;
	String value;

	public FractionRgb getFraction() {
		return fraction;
	}

	public int getR() {
		return r;
	}

	public int getG() {
		return g;
	}

	public int getB() {
		return b;
	}

	public String getValue() {
		return value;
	}

}
